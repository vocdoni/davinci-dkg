package helpers

import (
	"os"
	"path/filepath"
	"testing"

	"github.com/ethereum/go-ethereum/common"
	qt "github.com/frankban/quicktest"
)

func TestLoadHarnessConfig(t *testing.T) {
	c := qt.New(t)

	dir := t.TempDir()
	addressesPath := filepath.Join(dir, "addresses.env")
	err := os.WriteFile(addressesPath, []byte(`
REGISTRY=0x1000000000000000000000000000000000000001
MANAGER=0x2000000000000000000000000000000000000002
CONTRIBUTION_VERIFIER=0x3000000000000000000000000000000000000003
FINALIZE_VERIFIER=0x4000000000000000000000000000000000000004
PARTIAL_DECRYPT_VERIFIER=0x5000000000000000000000000000000000000005
DECRYPT_COMBINE_VERIFIER=0x6000000000000000000000000000000000000006
REVEAL_SUBMIT_VERIFIER=0x7000000000000000000000000000000000000007
REVEAL_SHARE_VERIFIER=0x8000000000000000000000000000000000000008
`), 0o600)
	c.Assert(err, qt.IsNil)

	t.Setenv("DAVINCI_DKG_TEST_RPC_URL", "http://127.0.0.1:8545")
	t.Setenv("DAVINCI_DKG_TEST_ADDRESSES", addressesPath)

	cfg, err := LoadHarnessConfigFromEnv()
	c.Assert(err, qt.IsNil)
	c.Assert(cfg.RPCURL, qt.Equals, "http://127.0.0.1:8545")
	c.Assert(cfg.Addresses.Registry, qt.Equals, common.HexToAddress("0x1000000000000000000000000000000000000001"))
}

